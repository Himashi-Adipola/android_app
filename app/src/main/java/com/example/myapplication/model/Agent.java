    public void setAgAddress(String agAddress) {
        this.agAddress = agAddress;
    }

    public String getAgGender() {
        return agGender;
    }

    public void setAgGender(String agGender) {
        this.agGender = agGender;
    }

    public String getAgDob() {
        return agDob;
    }

    public void setAgDob(String agDob) {
        this.agDob = agDob;
    }

